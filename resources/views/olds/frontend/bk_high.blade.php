	<script>
		$(document).ready(function(){
			// clock
			var countDownDate = new Date("<?php echo get_settings('clock_countdown_time'); ?>").getTime();
			var x = setInterval(function() {

				// Get todays date and time
				var now = new Date().getTime();

				// Find the distance between now an the count down date
				var distance = countDownDate - now;

				// Time calculations for days, hours, minutes and seconds
				var days = Math.floor(distance / (1000 * 60 * 60 * 24));
				var hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
				var minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
				var seconds = Math.floor((distance % (1000 * 60)) / 1000);

				// Display the result in the element with id="timer2"
				document.getElementById("timer2").innerHTML = '<ul class="countdown d-flex flex-wrap justify-content-center"><li><h2 class="title"><span class="days">' + days + '</span></h2><p class="days_text">days</p></li><li><h2 class="title"><span class="hours">' + hours + '</span></h2><p class="hours_text">hours</p></li><li><h2 class="title"><span class="minutes">' + minutes + '</span></h2><p class="minu_text">minutes</p></li><li><h2 class="title"><span class="seconds">' + seconds + '</span></h2><p class="seco_text">seconds</p></li></ul>';

				// If the count down is finished, write some text 
				if (distance < 0) {
					clearInterval(x);
					document.getElementById("timer2").innerHTML = "EXPIRED";
				}
			}, 1000);
		})
